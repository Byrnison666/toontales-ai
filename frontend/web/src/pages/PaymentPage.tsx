import { useEffect, useState } from 'react'
import { Link } from 'react-router-dom'
import { LegalLayout } from '../components/LegalLayout'
import { api, type SparkPackage } from '../api'
import { requisites } from '../lib/legal'

export function PaymentPage(): JSX.Element {
  const [packages, setPackages] = useState<SparkPackage[] | null>(null)

  useEffect(() => {
    let active = true
    // Прайс тянем с сервера, а не хардкодим: цена считается из себестоимости и
    // курса, и захардкоженная в вёрстке цифра разъехалась бы с реальным списанием.
    void api
      .getSparkPackages()
      .then((response) => {
        if (active) setPackages(response.packages)
      })
      .catch(() => undefined)
    return () => {
      active = false
    }
  }, [])

  return (
    <LegalLayout title="Оплата и получение" subtitle="Как оплатить услугу и получить готовый мультфильм.">
      <h2>Что вы покупаете</h2>
      <p>
        {requisites.brand} — онлайн-сервис по автоматическому созданию коротких анимированных видео (мультфильмов)
        из текста, который вы описываете. Это цифровая услуга: физические товары не отгружаются и не доставляются.
      </p>

      <h2>Как оплатить</h2>
      <p>
        Внутренняя валюта сервиса — кредиты («искры»). Вы пополняете баланс кредитов, оплачивая выбранный пакет
        банковской картой через платёжный сервис ЮKassa. Оплата проходит на защищённой странице ЮKassa; данные вашей
        карты сервису {requisites.brand} не передаются и не хранятся.
      </p>
      <p>
        После успешной оплаты исполнитель ({requisites.taxStatus}) формирует электронный чек и направляет его на
        указанный вами адрес электронной почты.
      </p>

      <h2>Пакеты искр</h2>
      {packages === null ? (
        <p>Актуальные цены пакетов показываются в разделе пополнения баланса.</p>
      ) : (
        <ul>
          {packages.map((item) => (
            <li key={item.sparks}>
              <b>{item.sparks.toLocaleString('ru-RU')} ✦</b> — {item.price_rub.toLocaleString('ru-RU')} ₽
            </li>
          ))}
        </ul>
      )}
      <p>
        Искры не сгорают и не имеют срока действия. Сколько роликов получится из пакета, зависит от их длительности:
        типичный ролик расходует около 3 300 искр.
      </p>

      <h2>Сколько списывается за ролик</h2>
      <p>
        Стоимость ролика зависит от того, каким он получится: сколько в нём сцен и какой они длительности. Поэтому при
        запуске сервис резервирует на балансе сумму по верхней границе — на самый длинный вариант ролика. Размер резерва
        показывается на странице «Создать» до запуска.
      </p>
      <p>
        Когда ролик готов, списывается фактическая стоимость выполненной работы, а неизрасходованный остаток резерва
        сразу возвращается на баланс. Как правило, итоговое списание заметно меньше зарезервированной суммы. Сколько
        списано за конкретный ролик, видно на его странице после завершения.
      </p>

      <h2>Как получить заказ</h2>
      <ul>
        <li>После оплаты кредиты автоматически зачисляются на баланс вашего аккаунта — обычно в течение нескольких минут.</li>
        <li>Вы запускаете создание мультфильма на странице «Создать»; на время работы часть баланса резервируется.</li>
        <li>Готовый ролик появляется в разделе «Мои мультфильмы», где его можно посмотреть и скачать.</li>
        <li>Ссылка на готовый файл доступна в вашем личном кабинете; отдельная физическая доставка не требуется.</li>
      </ul>

      <h2>Сроки</h2>
      <p>
        Генерация одного ролика обычно занимает несколько минут и зависит от загрузки сервиса. Если создание не удалось
        по технической причине, зарезервированные кредиты возвращаются на баланс автоматически в полном объёме.
      </p>

      <h2>Возвраты</h2>
      <p>
        Неизрасходованные кредиты возврату не подлежат. Остаток резерва возвращается на баланс автоматически после
        завершения генерации, а при техническом сбое резерв возвращается полностью.
        Подробнее — в <Link to="/offer">публичной оферте</Link>. Вопросы по оплате: <a href={`mailto:${requisites.email}`}>{requisites.email}</a>.
      </p>
    </LegalLayout>
  )
}
